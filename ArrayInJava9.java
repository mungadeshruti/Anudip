package com.corejava.demo;
class A
{
	int i;
}

public class ArrayInJava9 {
	static void methodOne(A[] a)
	{
		a[2].i=25;  //Changing the value field of A-type object referred by element at index 2
		a[5].i=45;  //Changing the value field of A-type object referred by element at index 5
	}

	public static void main(String[] args) {
		class A
		{
			int i;
		}


	}

}
